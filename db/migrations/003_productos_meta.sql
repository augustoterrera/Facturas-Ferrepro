create or replace function public.ferrepro_productos_set_search_vector()
  returns trigger
  language plpgsql
as $$
begin
  new.search_vector := to_tsvector('spanish', coalesce(new.search_text, ''));
  return new;
end;
$$;

drop trigger if exists trg_ferrepro_productos_search_vector on public.ferrepro_productos;
create trigger trg_ferrepro_productos_search_vector
  before insert or update of search_text
  on public.ferrepro_productos
  for each row execute function public.ferrepro_productos_set_search_vector();

